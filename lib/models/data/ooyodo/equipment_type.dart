  final Locale locale;

  late Map<int, String> subType2Map;

  EquipmentTypeUtil({required this.locale}) {
    subType2Map = switch (locale) {
      Locale(languageCode: 'en') => subType2En.asMap(),
      Locale(languageCode: 'ja') => subType2Ja.asMap(),
      Locale(languageCode: 'ko') => subType2Ko.asMap(),
      Locale(languageCode: 'zh', scriptCode: 'Hans') => subType2ZhHans.asMap(),
      Locale(languageCode: 'zh', scriptCode: 'Hant') => subType2ZhHant.asMap(),
      _ => subType2En.asMap(),
    };
  }

  String getSubType2Title(int subType2) {
    return subType2Map[subType2] ?? "";
  }
